CREATE TABLE KIND(
  ID UUID PRIMARY KEY,
  VORNAME VARCHAR(100) NOT NULL,
  GEBDATUM DATE
);

CREATE TABLE FACH (
    ID UUID PRIMARY KEY,
    VALUE varchar(255)
);

CREATE TABLE TESTART (
    ID UUID PRIMARY KEY,
    NAME varchar(255)
);

CREATE TABLE KATEGORIE (
    ID UUID PRIMARY KEY,
    FACHID UUID,
    NAME varchar(255)
);

CREATE TABLE NOTEN (
    ID UUID PRIMARY KEY,
    FACHID UUID,
    NAME varchar(255)
);

CREATE TABLE EINTRAG (
    ID UUID PRIMARY KEY,
    KINDID UUID,
    KLASSEID UUID,
    FACHID UUID,
    KATEGORIEID UUID,
    TESTARTID UUID,
    NOTEID UUID)
);